_TAG_RE = re.compile(r'<[^>]+>')


def strip_html(s):
    """A few templates above were written for the web UI's innerHTML
    rendering (<br>, <b>, <pre>...) - drop the markup for plain-text
    tkinter dialogs, keeping <br> as a line break."""
    s = s.replace('<br>', '\n')
    return _TAG_RE.sub('', s)


class Translator:
    """t(key, **params) resolves through the active language, falling back
    to English, and finally to the raw key if truly unknown."""

    def __init__(self, lang='en'):
        self.lang = lang if lang in MSG else 'en'

    def __call__(self, key, **params):
        tpl = MSG.get(self.lang, {}).get(key) or MSG['en'].get(key) or key
        return tpl.format(**params) if params else tpl

    def decode(self, raw, **_ignored):
        """Resolve an engine status code ("code" or "code:{json}") into
        localized, plain (HTML-stripped) text. Unknown codes are shown as-is
        - that's the fallback for anything not in the table below."""
        if not raw:
            return ''
        i = raw.find(':')
        code = raw if i == -1 else raw[:i]
        if code not in MSG['en']:
            return raw
        params = {}
        if i != -1:
            import json
            try:
                params = json.loads(raw[i + 1:])
            except Exception:
                return raw
        return strip_html(self(code, **params))
